-- Determines the width of a tab character in columns.
vim.bo.tabstop = 2

-- It specifies the amount of space to be inserted when the tab key is pressed and the amount to be removed when the backspace key is pressed.
vim.bo.softtabstop = 2

-- Defines the amount of whitespace used for one level of indentation, in columns.
vim.bo.shiftwidth = 2

